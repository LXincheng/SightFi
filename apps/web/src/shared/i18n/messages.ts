const DICTIONARY = {
  en: EN_MESSAGES,
  zh: ZH_MESSAGES,
} as const;

export type MessageKey = keyof typeof EN_MESSAGES;

let activeLocale: Locale = 'en';

export function setLocale(nextLocale: Locale): void {
  activeLocale = nextLocale;
}

export function resolveLocale(rawLocale: string): Locale {
  return rawLocale.toLowerCase().startsWith('zh') ? 'zh' : 'en';
}

export function t(
  key: MessageKey,
  vars?: Record<string, string | number>,
): string {
  const bundle = DICTIONARY[activeLocale];
  const template = bundle[key] ?? EN_MESSAGES[key] ?? key;
  if (!vars) return template;
  return Object.entries(vars).reduce(
    (text, [name, value]) =>
      text.replace(new RegExp(`\\{${name}\\}`, 'g'), String(value)),
    template,
  );
}
